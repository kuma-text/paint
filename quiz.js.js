let quizIndex = 0;
let quizResults = { IT: 0, Medicine: 0, Design: 0, Tech: 0 };

function runQuizEngine() {
    const card = document.getElementById("quiz-card-node");
    if (!card) return;

    if (quizIndex < extendedQuestions.length) {
        const qData = extendedQuestions[quizIndex];
        const progressPercent = Math.round((quizIndex / extendedQuestions.length) * 100);

        card.innerHTML = `
            <div class="quiz-progress-wrapper">
                <div style="display:flex; justify-content:space-between; font-size:0.9rem; color:var(--text-muted);">
                    <span>Вопрос ${quizIndex + 1} из ${extendedQuestions.length}</span>
                    <span>Выполнено: ${progressPercent}%</span>
                </div>
                <div class="quiz-bar-bg"><div class="quiz-bar-fill" style="width: ${progressPercent}%;"></div></div>
            </div>
            <h3 style="margin-bottom: 25px; line-height: 1.4; font-size: 1.25rem;">${qData.q}</h3>
            <div class="quiz-answers-stack">
                ${qData.opts.map(o => `<button class="quiz-opt-row" data-cat="${o.c}">${o.t}</button>`).join("")}
            </div>
        `;
    } else {
        renderQuizFinal();
    }
}

function renderQuizFinal() {
    const card = document.getElementById("quiz-card-node");
    const winningCategory = Object.keys(quizResults).reduce((a, b) => quizResults[a] > quizResults[b] ? a : b);

    const categoryTitles = {
        IT: "Информационные Технологии и ИИ",
        Medicine: "Биотехнологии и Прогрессивная Медицина",
        Design: "Цифровой Дизайн и Медиаискусство",
        Tech: "Высокотехнологичная Инженерия и Робототехника"
    };

    card.innerHTML = `
        <div style="text-align:center; padding: 20px 0;">
            <span style="font-size: 4rem; display:block; margin-bottom:15px;">📊</span>
            <h2>Профилирование завершено!</h2>
            <p style="color:var(--text-muted); margin: 15px 0;">Твоя ментальная структура наиболее склонна к сфере:</p>
            <h3 style="color:var(--secondary-color); font-size:1.6rem; margin-bottom:30px; font-family:var(--font-heading);">${categoryTitles[winningCategory]}</h3>
            <button class="btn-glow-primary" id="restart-quiz-trigger">Пройти тест повторно</button>
        </div>
    `;

    if (window.registerQuizResult) window.registerQuizResult(winningCategory);
}

document.addEventListener("click", (e) => {
    if (e.target.classList.contains("quiz-opt-row")) {
        const cat = e.target.dataset.category || e.target.getAttribute("data-cat");
        quizResults[cat]++;
        quizIndex++;
        runQuizEngine();
    }
    if (e.target.id === "restart-quiz-trigger") {
        quizIndex = 0;
        quizResults = { IT: 0, Medicine: 0, Design: 0, Tech: 0 };
        runQuizEngine();
    }
});